#include"iostream"

using namespace std;

int main()
{
    int a='a';
    cout  <<  "a="  <<  a  <<  endl;

    //see we input a='a' but the result is giving 97 that is because the ascii value of a is 97

    // let see another example

    char ch=98;

    cout  <<  "ch="  <<  ch  <<  endl;

    //see we input ch=98 but the result is showing b that is because the ascii value of b is 98

}
